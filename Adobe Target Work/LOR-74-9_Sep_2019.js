    function mblBtnClickAction() {
        const offersTitle = document.querySelectorAll('.news-section');
        const offerBtn = document.querySelector('.news-view-btn');
        const offerBtnText = document.querySelector('.news-view-btn-text');
        if (offersTitle && offerBtn && offerBtnText) {
            for (let i = 3; i < offersTitle.length; i += 1) {
              offersTitle[i].classList.add('news-hide');
            }
            offerBtn.addEventListener('click', () => {
                for (let i = 3; i < offersTitle.length; i += 1) {
                    if (offersTitle[i].classList.contains('news-hide')) {
                      offersTitle[i].classList.remove('news-hide');
                      offerBtn.classList.add('arrow-up');
                      offerBtnText.innerText = 'View Less Benefits';
                    } else {
                      offersTitle[i].classList.add('news-hide');
                      offerBtn.classList.remove('arrow-up');
                      offerBtnText.innerText = 'View All Benefits';
                    }
                }
            });
        }
    }

